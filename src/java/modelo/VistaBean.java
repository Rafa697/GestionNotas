package modelo;

public class VistaBean {

    private int coleccion;
    private double pi1;
    private double pi2;
    private double pi3;
    private double p1;
    private double p2;
    private double p3;
    private double notafinal;

    public VistaBean(int coleccion) {
        this.coleccion = coleccion;
    }

    public VistaBean() {
    }

    public int getColeccion() {
        return coleccion;
    }

    public void setColeccion(int coleccion) {
        this.coleccion = coleccion;
    }

    public double getPi1() {
        return pi1;
    }

    public void setPi1(double pi1) {
        this.pi1 = pi1;
    }

    public double getPi2() {
        return pi2;
    }

    public void setPi2(double pi2) {
        this.pi2 = pi2;
    }

    public double getPi3() {
        return pi3;
    }

    public void setPi3(double pi3) {
        this.pi3 = pi3;
    }

    public double getP1() {
        return p1;
    }

    public void setP1(double p1) {
        this.p1 = p1;
    }

    public double getP2() {
        return p2;
    }

    public void setP2(double p2) {
        this.p2 = p2;
    }

    public double getP3() {
        return p3;
    }

    public void setP3(double p3) {
        this.p3 = p3;
    }

    public double getNotafinal() {
        return notafinal;
    }

    public void setNotafinal(double notafinal) {
        this.notafinal = notafinal;
    }
    
    
}
